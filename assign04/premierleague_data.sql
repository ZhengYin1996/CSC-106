-- If the tables already exist, delete them --
drop table if exists stadiums;
drop table if exists players;
drop table if exists games;

-----------------------
-- Create the tables --
-----------------------

create table stadiums( team_name,
			city,
			stadium_name,
			capacity);

create table players( player_name,
			team_name,
			age,
			position,
			nationality);

create table games( 	date, 
			team_name, -- home team's name
			awayteam, 
			team_score, -- home team's score
			awayteam_score, 
			winner, -- one of H A D (home, away, draw resepctively)
			referee, 
			team_shots, -- home team's shots
			awayteam_shots,
			team_shotsontarget, -- home team's shots on target
			awayteam_shotsontarget, 
			team_fouls, -- home team's number of fouls
			awayteam_fouls, 
			team_corners, -- home team's number of corner kicks
			awayteam_corners, 
            team_yellowcards, -- home team's number of yellow cards
			awayteam_yellowcards, 
			team_redcards, -- home team's number of red cards
			awayteam_redcards);


